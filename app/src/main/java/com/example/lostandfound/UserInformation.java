package com.example.lostandfound;

public class UserInformation {

    public String name, sname, address, mobileNo, p1, p2;

    public UserInformation(){

    }

    public UserInformation(String name, String sname, String address, String mobileNo,  String p1, String p2) {
        this.name = name;
        this.sname= sname;
        this.address= address;
        this.mobileNo= mobileNo;
        this.p1=p1;
        this.p2=p2;

    }
}
